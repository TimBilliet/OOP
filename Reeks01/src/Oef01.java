public class Oef01 {
    public static void main(String[] args) {
        double x = 5;

        System.out.println("Het dubbel van " + x + " is "+  x*2);
        System.out.println("De helft van " + x + " is "+  x/2);
        System.out.println("Het kwadraat van " + x + " is " + x * x);
        System.out.println("De vierkantswortel van " + x + " is " + Math.sqrt(x));

        int y = 5;
        System.out.println();
        System.out.println("Het dubbel van " + y + " is "+  y*2);
        System.out.println("De helft van " + y + " is "+  y/2);
        System.out.println("Het kwadraat van " + y + " is " + y * y);
        System.out.println("De vierkantswortel van " + y + " is " + Math.sqrt(y));
    }

}